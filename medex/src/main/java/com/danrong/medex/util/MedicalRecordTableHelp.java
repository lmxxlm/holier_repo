package com.danrong.medex.util;

import java.util.HashMap;

public class MedicalRecordTableHelp {

  public static HashMap<String, Object> buildBaseRecord(HashMap<String, Object> data) {
    HashMap<String, Object> brmap = new HashMap<>();
    brmap.put(ParamName.medical_record_id, data.get(ParamName.medical_record_id));
    brmap.put(ParamName.record_id, data.get(ParamName.record_id));
    brmap.put(ParamName.table_id, data.get(ParamName.table_id));
    brmap.put(ParamName.table_name, data.get(ParamName.table_name));
    brmap.put(ParamName.patient_id, data.get(ParamName.patient_id));
    brmap.put(ParamName.input_time, data.get(ParamName.input_time));

    return brmap;
  }
}
